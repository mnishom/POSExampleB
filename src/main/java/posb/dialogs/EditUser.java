    private void editUser() {
        String nama = txtNama.getText();
        String jabatan = cmbJabatan.getSelectedItem().toString();
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());

        try {
            String Q = "UPDATE pegawai SET "
                    + "nama_pegawai=?,"
                    + "jabatan=?,"
                    + "username=?,"
                    + "password_hash=? "
                    + "WHERE "
                    + "id_pegawai=?";
            Connection C = Koneksi.Go();
            PreparedStatement P = C.prepareStatement(Q);
            P.setString(1, nama);
            P.setString(2, jabatan);
            P.setString(3, username);
            P.setString(4, password);
            P.setInt(5, Pg.getId());
            P.executeUpdate();//UPDATE data ke db

            ManageUsers.RefreshData(); // refresh data pada halaman kelola user
            this.setVisible(false); //tutup halaman/dialog
            //pesan/notifikasi
            JOptionPane.showMessageDialog(null, "Data sukses diubah");

        } catch (Exception e) {
        }
    }
